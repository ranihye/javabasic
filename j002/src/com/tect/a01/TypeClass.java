package com.tect.a01;

public class TypeClass {
	public static void main(String[] args) {
		String var1="가";//String은 문자열을 출력함. ""로 감싼다
		char c='a'; //char은 한글자만 가능, ''로 감싼다. char(문자)가 모여서 String(문자열)이 된다
		
		int var2=200;
		boolean flag=true;
		boolean onOff=false;//참과 거짓으로 구분할수 있음
				
		System.out.println(var1);
		System.out.println(c);
		System.out.println(var2);
		System.out.println(flag);
		System.out.println(onOff);
//	변수를 이용한 문자입력
//	문자를 프린트로 출력할수 있으나 문자 자체를 객체로써 사용 가능하다.
	}
}
